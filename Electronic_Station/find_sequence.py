"""
You are given a matrix of NxN (4≤N≤10). You should check if there is a sequence of 4 or more matching digits. The sequence may be positioned horizontally, vertically or diagonally (NW-SE or NE-SW diagonals).
"""
def checkio(matrix):
    #replace this for solution
    rotated = list(map(list, zip(*reversed(matrix))))

    for grid in (matrix, rotated):
        h, w = len(grid)-3, len(grid[0])-3
        for i, row in enumerate(grid):
            for j in range(w):
                if set(row[j:j+4]) == {row[j]}:
                    return True
                if i < h:
                    if set(grid[i+k][j+k] for k in range(4)) == {row[j]}:
                        return True
    return False

